print("\t---Manipulation de blocks de la fonction range()---")

print("\tLa fonction range() avec un seul paramètre")
for i in range(20):
    print(i)
print()

for i in range(20):
    print(i,end=" - ")
print()

print("\tLa fonction range() avec deux paramètres")
for i in range(10,20):
    print(i,end=" - ")
print()

print("\tLa fonction range() avec trois paramètres")
for i in range(10,20,2):
    print(i,end=" - ")


